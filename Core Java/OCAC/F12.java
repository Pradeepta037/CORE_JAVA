public class F12 {
    public static void main(String[] args) {
        float average = calculateAverage(4.5f, 7.8f);
        System.out.println("Average: " + average);
    }

    // Function that returns a float
    public static float calculateAverage(float num1, float num2) {
        return (num1 + num2) / 2;
    }
}
